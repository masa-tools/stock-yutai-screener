def _build_calendar() -> dict[int, list[dict]]:
    """288銘柄を月別に整理して返す"""
    cal:  dict[int, list[dict]] = {m: [] for m in range(1, 13)}
    seen: set[str] = set()

    for code, _ in get_candidates():
        if code in seen:
            continue
        seen.add(code)

        # 優待データ優先 → DEFAULT_KENRI → 3月（デフォルト）
        yi    = get_yutai(code)
        kenri = (yi.get("kenri_month") or "").strip() or DEFAULT_KENRI.get(code, "3月")
        yutai = yi.get("yutai", "―")
        # ④ JP_NAMES から日本語名を優先取得
        name  = JP_NAMES.get(code, f"コード {code}")

        # 「3月・9月」→ ["3月", "9月"] に分割
        for part in kenri.replace("・", " ").replace("、", " ").replace(",", " ").split():
            try:
                m = int(part.replace("月", ""))
                if 1 <= m <= 12:
                    cal[m].append({
                        "code" : code,
                        "name" : name,
                        "yutai": yutai,
                        "kenri": kenri,
                        "value": yi.get("yutai_value", 0),
                    })
            except ValueError:
                continue

    return cal


def render_calendar_tab() -> None:
    """配当・優待カレンダータブを描画する"""
    from candidate_stocks import get_candidates as _gc
    total_stocks = len(_gc())

    st.markdown(f"""
<div class="card" style="background:linear-gradient(135deg,#e8f5e9,#c8e6c9,#b2dfdb);
                          text-align:center;padding:1.1rem;">
    <div style="font-size:1.3rem;font-weight:700;color:#1b5e20;">📅 配当・優待カレンダー</div>
    <div style="color:#2e7d32;font-size:0.85rem;margin-top:0.25rem;">
        {total_stocks}銘柄の権利確定月を一覧表示（日本語銘柄名で表示）
    </div>
</div>
""", unsafe_allow_html=True)

    # ⑦ 1日キャッシュ（288銘柄分を毎回ループするのを避ける）
    from datetime import datetime
    today     = datetime.now().strftime("%Y-%m-%d")
    cache_key = f"calendar_{today}"

    if cache_key not in st.session_state:
        cal = _build_calendar()
        st.session_state[cache_key] = cal
    else:
        cal = st.session_state[cache_key]

    cur     = datetime.now().month
    ordered = list(range(cur, 13)) + list(range(1, cur))

    for m in ordered:
        items  = cal.get(m, [])
        count  = len(items)
        is_cur = (m == cur)
        label  = (
            f"{'🔔 ' if is_cur else ''}"
            f"{MONTH_LABELS[m]}"
            f"{'（今月）' if is_cur else ''}"
            f" — {count}銘柄"
        )

        with st.expander(label, expanded=is_cur):
            if count == 0:
                st.caption("この月が権利確定月の銘柄はありません")
                continue

            # ソート: 優待金額の高い順
            for item in sorted(items, key=lambda x: (-x["value"], x["code"])):
                code    = item["code"]
                name    = item["name"]
                yutai   = item["yutai"]
                val     = item["value"]
                yutai_s = yutai[:30] + "…" if len(yutai) > 30 else yutai
                val_tag = (
                    f' <span style="background:#fff9c4;color:#f57f17;'
                    f'border-radius:4px;padding:0 5px;font-size:0.72rem;">'
                    f'¥{val:,}</span>'
                    if val > 0 else ""
                )
                st.markdown(
                    f"<div style='padding:0.3rem 0;"
                    f"border-bottom:1px solid #e8f5e9;font-size:0.88rem;'>"
                    f"<span style='background:#e8f5e9;color:#2e7d32;"
                    f"border-radius:4px;padding:0.05rem 0.4rem;"
                    f"font-size:0.73rem;font-weight:600;margin-right:0.4rem;'>"
                    f"{code}</span>"
                    f"<strong style='color:#1b5e20;'>{name}</strong>"
                    f"<span style='color:#666;font-size:0.8rem;margin-left:0.4rem;'>"
                    f"{yutai_s}</span>{val_tag}</div>",
                    unsafe_allow_html=True)

    st.markdown("""
<div class="disclaimer" style="background:#e8f5e9;border-color:#81c784;">
    📌 権利確定月は推定値を含みます。実際の日程は各社の公式IRページでご確認ください。
</div>
""", unsafe_allow_html=True)
