@st.cache_data(ttl=1800)
def get_price_data(code: str, period: str = "1y") -> pd.DataFrame | None:
    """株価の時系列データ（OHLCV）を取得する"""
    try:
        ticker = yf.Ticker(f"{code}.T")
        df = ticker.history(period=period, auto_adjust=True)
        if df.empty:
            return None
        df.index = df.index.tz_localize(None)
        return df
    except Exception as e:
        st.error(f"データ取得エラー: {e}")
        return None


@st.cache_data(ttl=1800)
def get_stock_info(code: str) -> dict:
    """銘柄の基本情報を取得する"""
    try:
        ticker = yf.Ticker(f"{code}.T")
        info = ticker.info
        return info if info else {}
    except Exception:
        return {}


def fmt_yen(num) -> str:
    """大きな数字を「億円・兆円」で表示"""
    if num is None:
        return "―"
    try:
        n = float(num)
        if np.isnan(n):
            return "―"
        if n >= 1e12:
            return f"{n/1e12:.1f}兆円"
        if n >= 1e8:
            return f"{n/1e8:.0f}億円"
        return f"{n:,.0f}円"
    except Exception:
        return "―"


def fmt_num(val, decimals: int = 2, suffix: str = "") -> str:
    """数値を安全に文字列変換"""
    if val is None:
        return "―"
    try:
        f = float(val)
        if np.isnan(f) or np.isinf(f):
            return "―"
        return f"{f:.{decimals}f}{suffix}"
    except Exception:
        return "―"


def get_display_name(info: dict, code: str) -> str:
    """
    銘柄名を返す。JP_NAMESを優先して日本語名を返す。

    優先順:
      1. JP_NAMES（日本語マスター）
      2. yfinanceの longName / shortName
      3. コード番号
    """
    # ① 日本語マスターから取得（最優先）
    jp = JP_NAMES.get(code)
    if jp:
        return jp
    # ② yfinanceから取得
    return (
        info.get("longName") or
        info.get("shortName") or
        f"銘柄コード {code}"
    )
